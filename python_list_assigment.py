"""### Question 7: Write a Python program to find which number is palindrome in below given list .

- `sample input : `
    - `var = [134,121,155]`
- `sample output :`
    - `result : 134 is not a palindome , 121 is a palindrome `
"""

def palindrome(ele):
    return str(ele) == str(ele)[::-1]

def is_palindrome(var):
    results = []
    for ele in var:
        if palindrome(ele):
            results.append(f"{ele} is a palindrome")
        else:
            results.append(f"{ele} is not a palindrome")
    return ", ".join(results)

var = [134, 121, 151]
result = is_palindrome(var)

print("result:", result)

"""### Question 8: Write a Python program to find the common elements between two lists.

- `sample input : `
    - `var1 = [1,2,3,4,10]`
    - `var2 = [10,11,12,14]`
- `sample output :`
    - `result : 10`

    
"""

# Please write The solution Here for above question 8:
def common(var1,var2):
  var1=set(var1)
  var2=set(var2)
  common_ele=var1.intersection(var2)
  return list(common_ele)

var1=[1,2,3,4,10]
var2=[10,11,12,14]

res=common(var1,var2)
print(f"result :", (res))

